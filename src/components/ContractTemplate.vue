<script>
import html2pdf from "html2pdf.js";
import { mapActions, mapGetters } from "vuex";
import { v4 as uuidv4 } from "uuid";

import { DocumentService } from "@/services";

import FileSaver from "file-saver";

export default {
  props: {
    data: {
      type: Object,
      required: true,
      default: () => {},
    },
  },
  data() {
    return {
      loadingUpload: false,
      pdf: "",
    };
  },
  computed: {
    ...mapGetters([
      "getImagesCarTemplate",
      "getDescriptionData",
      "getVideoData",
      "getVehicleDataFromCache",
      "getProposal",
    ]),
    getCurrencyText() {
      switch (this.getVehicleDataFromCache.currency) {
        case "USD":
          return "Dólar";
        case "EUR":
          return "Euro";
        default:
          return "";
      }
    },
  },
  methods: {
    ...mapActions(["setLoadingState"]),
    async toDataUrl(url) {
      //Convert to base64
      return new Promise((resolve, reject) => {
        var xhr = new XMLHttpRequest();
        xhr.onload = function () {
          var reader = new FileReader();
          reader.onloadend = function () {
            resolve(reader.result);
          };
          reader.readAsDataURL(xhr.response);
        };
        xhr.onerror = () => {
          reject({
            status: this.status,
            statusText: xhr.statusText,
          });
        };
        xhr.open("GET", url);
        xhr.responseType = "blob";
        xhr.send();
      });
    },
    b64toBlob(b64Data, contentType = "", sliceSize = 512) {
      const byteCharacters = atob(b64Data);
      const byteArrays = [];

      for (
        let offset = 0;
        offset < byteCharacters.length;
        offset += sliceSize
      ) {
        const slice = byteCharacters.slice(offset, offset + sliceSize);

        const byteNumbers = new Array(slice.length);
        for (let i = 0; i < slice.length; i++) {
          byteNumbers[i] = slice.charCodeAt(i);
        }

        const byteArray = new Uint8Array(byteNumbers);
        byteArrays.push(byteArray);
      }

      const blob = new Blob(byteArrays, { type: contentType });
      return blob;
    },
    async makeProposal() {
      this.setLoadingState(true);
      this.$notify({
        title: "Atenção",
        message: "Gerando Proposta, por favor aguarde...",
        type: "warning",
      });
      let that = this;

      const id = uuidv4();
      if (this.getProposal && this.getProposal.id) {
        //Adicionar o ID da proposta no PDF
        document.getElementById("proposal-id").innerHTML = `Proposta: ${id}`;
      }

      const elm = document.getElementById("proposal-template");
      const date = that.$options.filters.formatDate(new Date().toISOString());

      const fileName = `Contrato ${that.getVehicleDataFromCache.product} em ${date}`;
      let opt = {
        enableLinks: true,
        margin: 1,
        image: { type: "jpeg", quality: 1 },
        filename: fileName,
        html2canvas: {
          dpi: 100,
          scale: 2,
          letterRendering: true,
          useCORS: true,
        },
        jsPDF: { unit: "in", format: "letter", orientation: "portrait" },
      };

      html2pdf()
        .set(opt)
        .from(elm)
        .outputPdf()
        .then(async function (pdf) {
          var contentType = "application/pdf";
          const blob = that.b64toBlob(btoa(pdf), contentType);

          that.setLoadingState(false);
          that.$notify({
            title: "Sucesso",
            message: "Proposta gerada com sucesso",
            type: "success",
          });

          that.savePdf(id, blob, fileName);

          setTimeout(() => {
            const date = that.$options.filters.formatDate(
              new Date().toISOString()
            );
            FileSaver.saveAs(
              blob,
              `Proposta ${that.getVehicleDataFromCache.product} em ${date}`
            );
          }, 1500);
        })
        .toPdf();
    },
    async savePdf(id, buffer, fileName) {
      if (this.getProposal && this.getProposal.id) {
        new DocumentService().save(id, buffer, this.getProposal.id, fileName);
      }
    },
  },
};
